    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if ( !usrFIRST_NAME.getText().isEmpty() &&
             usrFIRST_NAME.getText().length()<=30 &&
             !usrLAST_NAME.getText().isEmpty() && 
             usrLAST_NAME.getText().length()<=40) {
            
            if (CCardDeclaration.isSelected()) {
                Object[] Confirmation = {"Ναι", "Οχι"};
                Integer choiceC = JOptionPane.showOptionDialog(null,
                "Επιθυμείτε την προσθήκη πιστωτικής κάρτας;",
                "Προσθήκη κάρτας.",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                Confirmation,
                Confirmation[0]);
                /**
                 * * Αν επιλεχθεί η προσθήκη κάρτας, εμφανίζεται παράθυρο-διάλογος
                 * ο οποίος επιβεβαιώνει τα στοιχεία μέσω της εξωτερικής αρχής.
                 *  **
                 */
                if (choiceC == JOptionPane.YES_OPTION) {
                    CreditCardDialog dialog = new CreditCardDialog(frame,true);
                    dialog.setVisible(true);
                    
                    if (dialog.getReturnStatus() == 0) {
                    JOptionPane.showMessageDialog(null, "Δεν υπάρχει τέτοια κάρτα.");
                    CCardDeclaration.setSelected(false);
                    } else {
                        if (!createCustomer(dialog.getCCard().getPkCardId())){
                           JOptionPane.showMessageDialog(null, "Σφάλμα...!!!");
                        } else {
                            JOptionPane.showMessageDialog(null, "Πελάτης Δημιουργήθηκε!!!");
                            doClose(RET_OK);
                        }
                    }
                } else {
                    if (!createCustomer(0)){
                       JOptionPane.showMessageDialog(null, "Σφάλμα...!!!");
                    }else {
                       JOptionPane.showMessageDialog(null, "Πελάτης Δημιουργήθηκε!!!");
                       doClose(RET_OK);
                    }
                }
            } else {
                if (!createCustomer(0)){
                   JOptionPane.showMessageDialog(null, "Σφάλμα...!!!");
                }else {
                   JOptionPane.showMessageDialog(null, "Πελάτης Δημιουργήθηκε!!!");
                   doClose(RET_OK);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Εσφαλμένο μήκος "
                    + "ονόματος ή επωνύμου");
        }
    }//GEN-LAST:event_okButtonActionPerformed

