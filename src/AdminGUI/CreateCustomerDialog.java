    private boolean createCustomer(Integer ccard){
        Random ran = new Random(); //Κατασκευή γεννήτριας τυχαίων αριθμών
        Integer ran1 = ran.nextInt(1000);  //Δημιουργία αριθμών 0-999
        Integer ran2 = ran.nextInt(1000);
        String PointsCardNumber = String.format("%03d",ran1) //μετατροπή τους σε τριψήφιους
                +"-"+String.format("%03d",ran2); //και απόδοση του String στην κάρτα του πελάτη
        
        Customer cust = new Customer(usrFIRST_NAME.getText(), 
                                    usrLAST_NAME.getText(), 
                                    PointsCardNumber, 
                                    usrADDRESS.getText(),
                                    0,
                                    "01234568");
        if (ccard != 0) {
            cust.setCreditCardId(ccard);
        }
        try {
            loc.persist(cust);
            loc.getTransaction().commit();
            return true;
        } catch (Exception e) {
            e.printStackTrace();
            loc.getTransaction().rollback();
            return false;
        }
    }

